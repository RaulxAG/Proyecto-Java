    private void btnAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptarMouseClicked
        // TODO add your handling code here:
        
        try {
            String dni = txtDni.getText();
            String nombre = txtNombre.getText();
            String apellido1 = txtApellido1.getText();
            String apellido2 = txtApellido2.getText();
            Parentesto parentesto = null;
            
            String textoParentesto = txtParentesto.getText();
            
            if (textoParentesto.equalsIgnoreCase("TUTOR1")) {
                parentesto = Parentesto.TUTOR1;
            } else if (textoParentesto.equalsIgnoreCase("TUTOR2")) {
                parentesto = Parentesto.TUTOR2;
            } else if (textoParentesto.equalsIgnoreCase("OTROS")) {
                parentesto = Parentesto.OTROS;
            } else {
                // Manejar el caso en el que el texto no coincida con ninguna constante
                // Puede lanzar una excepción, mostrar un mensaje de error, asignar un valor predeterminado, etc.
                // Por ejemplo:
                JOptionPane.showMessageDialog(this, "Valor de Parentesto inválido: " + textoParentesto);
            }
            
        
            AutorizadoDaoImp adi = AutorizadoDaoImp.getInstance();
            Autorizado a = new Autorizado();

            a.setDni(dni);
            a.setNombre(nombre);
            a.setApellido1(apellido1);
            a.setApellido2(apellido2);
            a.setParentesto(parentesto);

            try {
                adi.add(a);
                this.dispose();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se han rellenado todos los campos.");
        }
        
    }//GEN-LAST:event_btnAceptarMouseClicked

