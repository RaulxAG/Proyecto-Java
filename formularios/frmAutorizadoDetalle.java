    private void cargaDatos(Autorizado a) {
        
        AutorizadoDaoImp adi = AutorizadoDaoImp.getInstance();
        
        try {
            Autorizado autorizado = adi.getById(a.getId());

            txtId.setText(String.valueOf(autorizado.getId()));
            txtDni.setText(autorizado.getDni());
            txtNombre.setText(autorizado.getNombre());
            txtApellido1.setText(autorizado.getApellido1());
            txtApellido2.setText(autorizado.getApellido2());
            txtParentesto.setText(String.valueOf(autorizado.getParentesto()));
            
        } catch (SQLException ex) {
            Logger.getLogger(frmAutorizadoDetalle.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    
